#include <stdlib.h>
#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	int	i;

	if (s == 0)
		return ;
	i = 0;
	while (n)
	{
		((char *)s)[i] = 0;
		i++;
		n--;
	}
}
