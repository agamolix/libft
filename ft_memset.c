#include <stdlib.h>
#include "libft.h"

void	*ft_memset(void *s, int c, size_t n)
{
    char *s2;
    int i;

    if (s == 0)
        return (0);
    s2 = s;
    i = 0;
	while (n)
    {
        s2[i] = c;
        i++;
        n--;
    }
	return (s);
}
