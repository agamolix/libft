#include "libft.h"

t_list *ft_lstlast(t_list *lst)
{
    t_list  *res;
    
    if (lst == 0)
        return (0);
    
    res = lst;
    while (res->next != 0)
    {
        res = res->next;
    }
    return (res);
}

