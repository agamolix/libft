#include <stdlib.h>
#include "libft.h"

t_list	*ft_lstnew(void *content)
{
	t_list	*res;

	res = malloc(sizeof(t_list));
	if (res == 0)
		return (0);
	res->content = content;
	res->next = 0;
	return (res);
}
