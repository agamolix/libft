#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *new)
{
	t_list	*temp;

	if (lst == 0)
		return ;
	temp = lst[0];
	while (temp->next != 0)
		temp = temp->next;
	temp->next = new;
}
